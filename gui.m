% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
name=edit1_KeyPressFcn(hObject, eventdata, handles);
try
    img=imread(name);
catch
    f=errordlg('Image not found');
    return;
end

tresh=popupmenu1_KeyPressFcn(hObject, eventdata, handles)
if tresh~=1
    if tresh==2
        res=sixconn(img,0);
    elseif tresh==3
        res=sixconn(img,0.1);
    elseif tresh==4
        res=sixconn(img,0.2);
    elseif tresh==5
        res=sixconn(img,0.3);
    elseif tresh==6
        res=sixconn(img,0.4);
    elseif tresh==7
        res=sixconn(img,0.5);
    elseif tresh==8
        res=sixconn(img,0.6);
    elseif tresh==9
        res=sixconn(img,0.7);
    elseif tresh==10
        res=sixconn(img,0.8);
    elseif tresh==11
        res=sixconn(img,0.9);
    end
    f=figure()
    subplot(1,2,1);
    imshow(img,[]);
    title('Original Image');
    subplot(1,2,2);
    imshow(res,[]);
    title('Objects Found');
else
    f=errordlg('Select proper treshold value');
    return;
end


